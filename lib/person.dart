// person.dart
class Person {
  String name;
  String surname;
  String idNumber;
  String mobileNumber;
  String email;
  String birthDate;
  String language;
  List<String> interests;

  Person({
    required this.name,
    required this.surname,
    required this.idNumber,
    required this.mobileNumber,
    required this.email,
    required this.birthDate,
    required this.language,
    required this.interests,
  });
}
